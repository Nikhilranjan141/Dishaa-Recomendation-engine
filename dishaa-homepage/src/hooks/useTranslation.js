export const useTranslation = () => {
  const { currentLanguage } = useLanguage();

  const t = (text) => {
    // If English, return original text
    if (currentLanguage === 'en') {
      return text;
    }

    // Check if translation exists in dictionary
    if (translationDictionary[text] && translationDictionary[text][currentLanguage]) {
      return translationDictionary[text][currentLanguage];
    }

    // If no translation found, return original text with language indicator (for demo)
    return `${text}`;
  };

  return { t, currentLanguage };
};